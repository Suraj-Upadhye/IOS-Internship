// 6.	Write a program that uses a switch statement to print the day of the week. Given a number between 1 and 7, print:
// 1: "Monday"
// 2: "Tuesday"
// 3: "Wednesday"
// 4: "Thursday"
// 5: "Friday"
// 6: "Saturday"
// 7: "Sunday"

let day = 1
switch day {
    case 1:
        print("Monday")
    case 2:
        print("Tuesday")
    case 3:
        print("Wednesday")
    case 4:
        print("Thursday")
    case 5:
        print("Friday")
    case 6:
        print("Saturday")
    case 7:
        print("Sunday")
    default:
        print("Invalid Day")
}